package chess.pieces;

/**
 * Piece colors in the game of chess.
 * 
 * @author Marco Olea
 * @version 1.0
 * @see chess.pieces.Piece
 */
public enum Color {
    /** Denotes a white piece. */
    WHITE,
    /** Denotes a black piece. */
    BLACK,
    /** Used for representing empty squares on the board. */
    NONE
}